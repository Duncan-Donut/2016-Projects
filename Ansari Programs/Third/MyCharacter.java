public class MyCharacter{
	
	//Variables
	private String race;
	private int strength;
	private int lives;
	
	//Setters
	public void setRace(String value){
	    race = value;	
	}
	public void setStrength(int value){
	    strength = value;	
	}
	public void setLives(int value){
	    lives = value;	
	}
	
	//Getters
	public String getRace(){
	    return race;	
	}
	public int getStrength(){
	    return strength;	
	}
	public int getLives(){
	    return lives;	
	}
	
	
	
	
	
}